    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        String user = request.getParameter("user");
        String pass = request.getParameter("pass");
        String newPa = request.getParameter("newPass");
        String confirmPass = request.getParameter("confirmPass");
        DAO dao = new DAO();
        Account ac = dao.checkAccountExist(user);
        if (dao.checkAccountExist(user) != null && pass.equals(ac.getPass())) {
            if (newPa.equals(confirmPass)) {
                dao.updatePass(newPa, user);
                response.sendRedirect("countPro");
            } else {
                String mess = "Confirm pass must be the same new pass!";
                request.setAttribute("mess", mess);
                request.getRequestDispatcher("change-newpassword.jsp").forward(request, response);
            }

        } else {
            String mess = "Account not exist!";
            request.setAttribute("mess", mess);
            request.getRequestDispatcher("change-newpassword.jsp").forward(request, response);
        }
    }

